// Addition of two numbers.

#include <iostream>
using namespace std;

int main()
{
    int a;
    int b;
    int sum;
    cout << "Enter number a: ";
    cin >> a;
    cout << "Enter number b: ";
    cin >> b;
    sum = a + b;
    cout << "Sum of " << a << " & " << b << " is: " << sum;
    return 0;
}