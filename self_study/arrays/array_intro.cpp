/*
    Program: Array Introduction
    Description:
    Demonstrates how to declare, initialize, and access elements of an array
    using their index.
*/

#include <iostream>
using namespace std;

int main()
{
    int arr[5] = {25, 12, 33, 54, 78};

    cout << "Index 0 : " << arr[0] << endl;
    cout << "Index 1 : " << arr[1] << endl;
    cout << "Index 2 : " << arr[2] << endl;
    cout << "Index 3 : " << arr[3] << endl;
    cout << "Index 4 : " << arr[4] << endl;
    return 0;
}
